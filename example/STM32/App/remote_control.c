//判断遥控器数据是否出错，
uint8_t RC_data_is_error(void)
{
    //使用了go to语句 方便出错统一处理遥控器变量数据归零
    if (RC_abs(rc_ctrl.rc.ch[0]) > RC_CHANNAL_ERROR_VALUE)
    {
        goto error;
    }
    if (RC_abs(rc_ctrl.rc.ch[1]) > RC_CHANNAL_ERROR_VALUE)
    {
        goto error;
    }
    if (RC_abs(rc_ctrl.rc.ch[2]) > RC_CHANNAL_ERROR_VALUE)
    {
        goto error;
    }
    if (RC_abs(rc_ctrl.rc.ch[3]) > RC_CHANNAL_ERROR_VALUE)
    {
        goto error;
    }
    if (rc_ctrl.rc.s[0] == 0)
    {
        goto error;
    }
    if (rc_ctrl.rc.s[1] == 0)
    {
        goto error;
    }
    return 0;

error:
    rc_ctrl.rc.ch[0] = 0;
    rc_ctrl.rc.ch[1] = 0;
    rc_ctrl.rc.ch[2] = 0;
    rc_ctrl.rc.ch[3] = 0;
    rc_ctrl.rc.ch[4] = 0;
    rc_ctrl.rc.s[0] = RC_SW_DOWN;
    rc_ctrl.rc.s[1] = RC_SW_DOWN;
    rc_ctrl.mouse.x = 0;
    rc_ctrl.mouse.y = 0;
    rc_ctrl.mouse.z = 0;
    rc_ctrl.mouse.press_l = 0;
    rc_ctrl.mouse.press_r = 0;
//    rc_ctrl.key.v = 0;
    return 1;
}

void RC_rocker_drift_resolution(RC_ctrl_t *rc_ctrl)
{
	if(RC_abs(rc_ctrl->rc.ch[0])<RC_MIN_LIMIT)
		rc_ctrl->rc.ch[0] = 0;
	if(RC_abs(rc_ctrl->rc.ch[1])<RC_MIN_LIMIT)
		rc_ctrl->rc.ch[1] = 0;
	if(RC_abs(rc_ctrl->rc.ch[2])<RC_MIN_LIMIT)
		rc_ctrl->rc.ch[2] = 0;
	if(RC_abs(rc_ctrl->rc.ch[3])<RC_MIN_LIMIT)
		rc_ctrl->rc.ch[3] = 0;
}

